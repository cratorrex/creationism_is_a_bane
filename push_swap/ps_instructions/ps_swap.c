#include "../push_swap.h"

int	**ps_swap(int **stack)
{
	int	temp[2];

	temp[0] = stack[0][0];
	temp[1] = stack[0][1];
	stack[0][0] = stack[1][0];
	stack[0][1] = stack[1][1];
	stack[1][0] = temp[0];
	stack[1][1] = temp[1];
	return (stack);
}
